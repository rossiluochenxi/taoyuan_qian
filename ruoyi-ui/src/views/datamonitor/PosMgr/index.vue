<script>
import { listRtdata, getRtdata, delRtdata, addRtdata, updateRtdata } from "@/api/dm/rtdata";

export default {
  name: "Map",
  data() {
    return {
       // 遮罩层
       loading: true,
      // 选中数组
      ids: [],
      // 非单个禁用
      single: true,
      // 非多个禁用
      multiple: true,
      // 显示搜索条件
      showSearch: true,
      // 总条数
      total: 0,
      // 冻结数据表格数据
      rtdataList: [],
      // 弹出层标题
      title: "",
      // 是否显示弹出层
      open: false,
      // 查询参数
      queryParams: {
        pageNum: 1,
        pageSize: 10,
        agroLivestockCode: null,
        agroLivestockIccid: null,
        agroLivestockXqiccid: null,
        temperature: null,
        step: null,
        weight: null,
        livestockLon: null,
        livestockLat: null,
        rsrq: null,
        ecl: null,
        cellid: null,
        snr: null,
        rsrp: null,
        pci: null,
        date: null
      },
      // 表单参数
      form: {},
      // 表单校验
      rules: {
      },
      map: null,
      farmerName: "",
      earTagNumber: "",
      deviceNumber: "",
      localList:[]
   
    };
  },
  mounted() {
    this.initMap();
  },
  created() {
    this.getList();
  },
  methods: {
    initMap() {
      this.map = new AMap.Map("mapContainer", {
        zoom: 10,
        center: [116.396, 39.919],
        resizeEnable: true
      });
    },
    search() {
      // 执行查询操作
    },
    locateOnMap(rows) {
      this.localList = [];
      this.localList.push([rows.livestockLon, rows.livestockLat]);
      
      const location = this.localList[0]; // 或者 const location = [...this.localList];
      // console.log("按钮事件    :"+rows.livestockLon);
      // console.log("按钮事件2    :"+this.localList[0]);
      // console.log("按钮事件3   :"+location);
      // console.log("按钮事件4  :"+this.localList);

      // console.log("rows.livestockLon  :"+rows.livestockLon +"rows.livestockLat  :"+rows.livestockLat  );



      if (location) {
        const marker = new AMap.Marker({
          position: location,
          map: this.map
        });
      
        const infoWindowContent = `
          <div>
            <p>养殖户：${rows.agroUserName}</p>
            <p>电话：</p>
            <p>牲畜标号：${rows.agroLivestockCode}</p>
            <p>设备编号：${rows.agroLivestockIccid}</p>
            <p>牛品种：</p>
            <p>经度：${location[0]}</p>
            <p>纬度：${location[1]}</p>
          </div>
        `;

        const infoWindow = new AMap.InfoWindow({
          content: infoWindowContent
        });

        infoWindow.open(this.map, location);  
        this.map.setCenter(location);

        // 在地图上添加定位的小图标
        const currentLocationMarker = new AMap.Marker({
          position: location,
          icon: '/assets/images/xiaoniu.png', // 这里替换成你想要的小图标的 URL
          offset: new AMap.Pixel(-12, -36), // 图标的偏移量，可以根据实际情况调整
          map: this.map
        });
      }
    },


     /** 查询冻结数据列表 */
     getList() {
      this.loading = true;
      listRtdata(this.queryParams).then(response => {
        this.rtdataList = response.rows;
        this.total = response.total;
        this.loading = false;
      });
    },
   formatDate(dateString) {
    const date = new Date(dateString);
    const year = date.getFullYear();
    let month = date.getMonth() + 1;
    month = month < 10 ? '0' + month : month;
    let day = date.getDate();
    day = day < 10 ? '0' + day : day;
    let hours = date.getHours();
    hours = hours < 10 ? '0' + hours : hours;
    let minutes = date.getMinutes();
    minutes = minutes < 10 ? '0' + minutes : minutes;
    let seconds = date.getSeconds();
    seconds = seconds < 10 ? '0' + seconds : seconds;
    return `${year}-${month}-${day} ${hours}:${minutes}:${seconds}`;
  },
    // 取消按钮
    cancel() {
      this.open = false;
      this.reset();
    },
    // 表单重置
    reset() {
      this.form = {
        id: null,
        createBy: null,
        createTime: null,
        updateBy: null,
        updateTime: null,
        agroUserId: null,
        agroUserName: null,
        agroLivestockId: null,
        agroLivestockCode: null,
        agroLivestockIccid: null,
        agroLivestockXqiccid: null,
        imei: null,
        deviceId: null,
        temperature: null,
        step: null,
        weight: null,
        livestockLon: null,
        livestockLat: null,
        rsrq: null,
        ecl: null,
        cellid: null,
        snr: null,
        rsrp: null,
        pci: null,
        date: null
      };
      this.resetForm("form");
    },
    /** 搜索按钮操作 */
    handleQuery() {
      this.queryParams.pageNum = 1;
      this.getList();
    },
    /** 重置按钮操作 */
    resetQuery() {
      this.resetForm("queryForm");
      this.handleQuery();
    },
    // 多选框选中数据
    handleSelectionChange(selection) {
      this.ids = selection.map(item => item.id)
      this.single = selection.length!==1
      this.multiple = !selection.length
    },
    /** 新增按钮操作 */
    handleAdd() {
      this.reset();
      this.open = true;
      this.title = "添加冻结数据";
    },
    /** 修改按钮操作 */
    handleUpdate(row) {
      this.reset();
      const id = row.id || this.ids
      getRtdata(id).then(response => {
        this.form = response.data;
        this.open = true;
        this.title = "修改冻结数据";
      });
    },
    /** 提交按钮 */
    submitForm() {
      this.$refs["form"].validate(valid => {
        if (valid) {
          if (this.form.id != null) {
            updateRtdata(this.form).then(response => {
              this.$modal.msgSuccess("修改成功");
              this.open = false;
              this.getList();
            });
          } else {
            addRtdata(this.form).then(response => {
              this.$modal.msgSuccess("新增成功");
              this.open = false;
              this.getList();
            });
          }
        }
      });
    },
    /** 删除按钮操作 */
    handleDelete(row) {
      const ids = row.id || this.ids;
      this.$modal.confirm('是否确认删除冻结数据编号为"' + ids + '"的数据项？').then(function() {
        return delRtdata(ids);
      }).then(() => {
        this.getList();
        this.$modal.msgSuccess("删除成功");
      }).catch(() => {});
    },
    /** 导出按钮操作 */
    handleExport() {
      this.download('dm/rtdata/export', {
        ...this.queryParams
      }, `rtdata_${new Date().getTime()}.xlsx`)
    }
  
  }
};
